a=int(input('Enter a  First number'))
b=int(input('Enter a  Second number'))
c=str(input('Enter a operator'))
if(c=='+'):
    print('Your answer is:',a+b)
elif(c=='-'):
    print('Your answer is:',a-b)
elif(c=='*'):
    print('Your answer is:',a*b)
elif(c=='/'):
    print('Your answer is:',a/b)
elif(c=='%'):
    print('Your answer is:',a%b)